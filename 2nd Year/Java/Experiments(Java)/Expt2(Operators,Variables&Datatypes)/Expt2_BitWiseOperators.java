public class Expt2_BitWiseOperators {
    public static void main(String args[]){
        int a=5,b=2,c=4;
        System.out.println("a=5,b=2,c=4;");
        System.out.println("A & B = "+(a&b));
        System.out.println("A | B = "+(a|b));
        System.out.println("C ^ b = "+(c^b));
        System.out.println("~A = "+(~a));
    }
}

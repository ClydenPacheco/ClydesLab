public class Expt2_BasicOperators {
    public static void main(String [] args) {
        int a=8,b=2;   
        System.out.println ("For integer values:");   
        System.out.println ("When a="+a+" and b="+b);   
        System.out.println("The sum of "+a+"+"+b+"="+(a+b));   
        System.out.println ("The diference between "+a+"-"+b+"="+(a-b));
        System.out.println ("The product of "+a+"*"+b+"="+(a*b));
        System.out.println ("The quotient of "+a+"/"+b+"="+(a/b));
        System.out.println("The remainder of "+a+"%"+b+"="+(a%b));
        float c=5.4f,d=3.7f;   
        System.out.println("For floating point values:");   
        System.out.println ("When c="+c+" and d="+d); 
        System.out.println ("The sum of "+c+"+"+d+"="+(c+d));   
        System.out.println ("The diffrence between "+c+"-"+d+"="+(c-d));
        System.out.println ("The product of "+c+"*"+d+"="+(c*d));
        System.out.println ("The quotinent of "+c+"/"+d+"="+(c/d));
        System.out.println ("The remainder of "+c+"%"+d+"="+(c%d));
    }
}
